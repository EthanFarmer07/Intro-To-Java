// This program does a division problem.
public class Compute_expressions {

	public static void main(String[] args) {
		// Variable for solving the math problem.
		double ans = (((9.5 * 4.5) - (2.5 * 3)) / (45.5 - 3.5));
		
		// Print so the problem can be seen.
		System.out.println("9.5 X 4.5 - 2.5 X 3");
		System.out.println("-------------------");
		System.out.println("     45.5 - 3.5");
		System.out.println("         =");
		
		// Gets the answer from the variable.
		System.out.println(ans);
	}
}
